#include <math.h>
#include <stdio.h>
#include <ctype.h>

int main()
{
    char c;
    printf("enter character: ");
    scanf("%c", &c);

    if (isupper)
    {
        printf("uppercase");
    }
    else if (islower)
    {
        printf("lowercase");
    }
    else if (isalnum)
    {
        printf("number");
    }
    else
    {
        printf("special character");
    }
}